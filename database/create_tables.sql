CREATE TYPE ROLE_NAME AS ENUM ('admin', 'manager', 'customer');

CREATE TABLE IF NOT EXISTS ROLES (
    ROLE_ID SERIAL PRIMARY KEY,
    ROLE_NAME ROLE_NAME,
    DESCRIPTION VARCHAR(128),
    PERMISSIONS VARCHAR(128)
);

